const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const migrationsDir = path.join(__dirname, '../../supabase/migrations');
const outputFile = path.join(__dirname, '../../supabase/rebuild_migration_engine.sql');

const expectedMigrations = [
  '20241117_add_core_tables.sql',
  '20250101_drop_sensitive_tables.sql',
  '20250101000000_vella_core_admin.sql',
  '20250217_token_engine.sql',
  '20250218_add_adaptive_traits.sql',
  '20250219_add_nudge_history.sql',
  '20250220_add_feature_tables.sql',
  '20250221_add_progress_features.sql',
  '20250222_add_last_active_at.sql',
  '20250223_remove_checkin_note.sql',
  '20251129154622_create_admin_global_config.sql',
  '20251219_drop_legacy_vella_settings_fields.sql',
  '20251220_fix_vella_settings.sql',
];

function computeSHA256(content) {
  return crypto.createHash('sha256').update(content, 'utf8').digest('hex');
}

function escapeSQLString(str) {
  return str.replace(/'/g, "''").replace(/\\/g, '\\\\');
}

function processMigrations() {
  const migrations = [];

  for (const filename of expectedMigrations) {
    const filePath = path.join(migrationsDir, filename);
    const content = fs.readFileSync(filePath, 'utf8');
    const hash = computeSHA256(content);
    
    const match = filename.match(/^(\d{8,14})_(.+)\.sql$/);
    if (!match) {
      throw new Error(`Invalid migration filename: ${filename}`);
    }
    
    const version = match[1];
    const name = match[2];
    
    migrations.push({
      version,
      name,
      filename,
      hash,
      statements: content,
    });
  }

  return migrations;
}

function generateSQL(migrations) {
  let sql = `-- ============================================================================
-- Supabase Migration Engine Rebuild Script
-- ============================================================================
-- This script completely rebuilds the migration engine tables.
-- Run this ONCE in the Supabase SQL Editor to normalize the migration system.
-- ============================================================================

BEGIN;

-- Drop old schema_migrations table if it exists
DROP TABLE IF EXISTS supabase_migrations.schema_migrations CASCADE;

-- Ensure schema exists
CREATE SCHEMA IF NOT EXISTS supabase_migrations;

-- Create migrations table with correct schema
CREATE TABLE IF NOT EXISTS supabase_migrations.migrations (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version text NOT NULL,
    name text NOT NULL,
    hash text NOT NULL,
    executed_at timestamptz NOT NULL DEFAULT now(),
    statements text NOT NULL
);

-- Truncate existing data
TRUNCATE TABLE supabase_migrations.migrations;

-- Insert all migrations
`;

  for (const migration of migrations) {
    const escapedStatements = escapeSQLString(migration.statements);
    sql += `INSERT INTO supabase_migrations.migrations (version, name, hash, executed_at, statements)
VALUES ('${migration.version}', '${migration.name}', '${migration.hash}', now(), '${escapedStatements}');

`;
  }

  sql += `COMMIT;

-- ============================================================================
-- Validation Query (run after commit to verify)
-- ============================================================================
-- SELECT version, name, hash, executed_at 
-- FROM supabase_migrations.migrations 
-- ORDER BY version;
`;

  return sql;
}

function generateValidationScript(migrations) {
  let validation = `-- ============================================================================
-- Migration Validation Script
-- ============================================================================
-- Run this to verify all migrations are correctly inserted
-- ============================================================================

SELECT 
    version,
    name,
    hash,
    executed_at,
    LENGTH(statements) as statements_length
FROM supabase_migrations.migrations
ORDER BY version;

-- Expected count: ${migrations.length}
SELECT COUNT(*) as total_migrations FROM supabase_migrations.migrations;

-- Expected hashes:
`;

  for (const migration of migrations) {
    validation += `-- ${migration.version}: ${migration.hash}\n`;
  }

  return validation;
}

// Main execution
try {
  console.log('Processing migrations...');
  const migrations = processMigrations();
  
  console.log(`Found ${migrations.length} migrations`);
  console.log('Computing hashes and generating SQL...');
  
  const sql = generateSQL(migrations);
  fs.writeFileSync(outputFile, sql, 'utf8');
  
  console.log(`✅ SQL file generated: ${outputFile}`);
  
  // Generate validation script
  const validationScript = generateValidationScript(migrations);
  const validationFile = path.join(__dirname, '../../supabase/validate_migrations.sql');
  fs.writeFileSync(validationFile, validationScript, 'utf8');
  
  console.log(`✅ Validation script generated: ${validationFile}`);
  
  // Print summary
  console.log('\n=== Migration Summary ===');
  for (const migration of migrations) {
    console.log(`${migration.version}: ${migration.name} (hash: ${migration.hash.substring(0, 16)}...)`);
  }
  
  console.log('\n✅ Rebuild script generation complete!');
} catch (error) {
  console.error('Error:', error);
  process.exit(1);
}

